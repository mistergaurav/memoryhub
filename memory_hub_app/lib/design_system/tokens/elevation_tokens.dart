/// Material 3 elevation levels
/// Use these instead of hardcoded elevation values
class Elevations {
  Elevations._();

  /// Level 0 - No elevation (flat surface)
  static const double level0 = 0.0;

  /// Level 1 - Minimal elevation
  static const double level1 = 1.0;

  /// Level 2 - Low elevation (cards, chips)
  static const double level2 = 3.0;

  /// Level 3 - Medium elevation (FAB, dialogs)
  static const double level3 = 6.0;

  /// Level 4 - High elevation (navigation drawer)
  static const double level4 = 8.0;

  /// Level 5 - Highest elevation (modal bottom sheet)
  static const double level5 = 12.0;
}
