/// Border width tokens
/// Use these instead of hardcoded border width values
class Borders {
  Borders._();

  /// 1px - Thin border
  static const double thin = 1.0;

  /// 1.5px - Regular border
  static const double regular = 1.5;

  /// 2px - Thick border
  static const double thick = 2.0;

  /// 3px - Extra thick border (focus indicators)
  static const double extraThick = 3.0;
}
